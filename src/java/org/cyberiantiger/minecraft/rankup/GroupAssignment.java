package org.cyberiantiger.minecraft.rankup;

public class GroupAssignment {
    private final String group = null;
    private final String world = null;


    public String getGroup() {
        return group;
    }

    public String getWorld() {
        return world;
    }

    @Override
    public String toString() {
        return "GroupAssignment{" + "group=" + group + ", world=" + world + '}';
    }
}